export function formatDate(dateString: string, language: "sr" | "en"): string {
  const date = new Date(dateString)
  return date.toLocaleDateString(language === "sr" ? "sr-RS" : "en-US", {
    year: "numeric",
    month: "long",
    day: "numeric",
  })
}
